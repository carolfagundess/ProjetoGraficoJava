    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        if (listDisciplinas.getSelectedIndex() >=0 ) {
            int result = JOptionPane.showConfirmDialog(this, "Tem certeza?", "Confirmação de remoção", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (result == JOptionPane.YES_OPTION) {
                    Main.listaD.remove(listDisciplinas.getSelectedIndex());
                    atualizaLista();
            }
        }else{
            JOptionPane.showMessageDialog(this, "Selecione uma disciplina para remover!");
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void listDisciplinasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listDisciplinasMouseClicked
        if (evt.getClickCount() == 2) {
            if (listDisciplinas.getSelectedIndex() >= 0) {
                TelaCadastro telaCad = new TelaCadastro();
                telaCad.setIndex(listDisciplinas.getSelectedIndex());
                telaCad.setModal(true);
                telaCad.setVisible(true);
                atualizaLista();
            }
        }
    }//GEN-LAST:event_listDisciplinasMouseClicked

