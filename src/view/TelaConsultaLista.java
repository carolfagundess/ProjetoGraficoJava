    private void atualizaLista() {
        DefaultListModel model = new DefaultListModel();
        for (Disciplina d : Main.listaD.getList()) {
            model.addElement(d.getNome() + " (" + d.getProfessor() + ")");
        }

        listDisciplinas.setModel(model);
    }

